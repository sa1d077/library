#include "libft.h"

size_t		ft_strlcat(char *restrict dst, const char *restrict src,
		size_t size)
{
	char	*cur;
	char	*reader;
	size_t	len;

	cur = (char *)ft_memchr(dst, '\0', size);
	if (cur == NULL)
		return (size + ft_strlen((char *)src));
	reader = (char *)src;
	len = (size_t)(cur - dst) + ft_strlen((char *)reader);
	while ((size_t)(cur - dst) < size - 1 && *reader != '\0')
	{
		*cur = *reader;
		cur++;
		reader++;
	}
	*cur = '\0';
	return (len);
}
